                    <h3 class="mtn">Jagamana</h3>
                    <p>Jagamana adalah website marketplace yang khususkan diri dalam bidang medis. Jagamana menjadi media dan mitra antara perusahaan, distributor, rumah sakit, dan instansi health care lainnya untuk memberikan pelayanan, informasi dan kesempatan untuk para profesional menemukan karir dan masa depan. Kami mengembangkan fitur, website, dengan usaha semaksimal mungkin untuk menciptakan tempat yang nyaman, aman dan berpartisipasi dalam persaingan global.</p>
                    <p>Kami memberikan pelayanan website 24/7 dengan selalu online dan konfirmasi secepat mungkin. Memberikan dan menciptakan ruang informasi. Jagamana menjaga kerahasiaan dan bekerja secara profesional.</p>
                    <h3>User</h3>
                    <p>Setiap orang dapat memuat akun sebagai "Employee", level akun ini merupakan user standar dimana setiap akun dapat mendaftar pekerjaan yang tersedia, menciptakan thread dan mengelolanya di forum yang disediakan. employee dapat mengupload dan membuat Curicullum Vitae di halaman dashboard. Apply pekerjaan tidak dibatasi jumlah atau kuota per hari, setiap pekerjaan yang ditawar oleh employee akan direview oleh admin untuk formalitas dan validasi.</p>
                    <h3>Pekerjaan</h3>
                    <p>Jagamana merupakan website yang menyediakan daftar pekerjaan dibidang medis. Pekerjaan yang terdaftar di Jagamana telah diketahui dan dikonfirmasi oleh Administrator pengelola user account. Pekerjaan yang tersedia dari berbagai kota dan negara, dengan berbagai kualifikasi dan requirement. Bidang peerjaan meliputi Officer, Manager, Researcher, Doctor, Field Worker dan lain sebagainya.</p>
                    <h3>Perusahaan</h3>
                    <p>Company merupakan akun khusus yang memiliki kewenangan menciptakan lowongan pekerjaan dan kesempatan berkarir. perusahaan yang terdaftar harus dalam cakupan bidang medis. perusahaan dapat menerima lamaran, mengelola follower dan dokumen CV dari Employee dengan konfirmasi Administrator Jagamana tentunya. Perusahaan harus memberikan informasi kantor dan pekerjaan dengan jelas, telah disediakan halaman khusus tentang kepegawaian, tempat kerja, visi misi, deskripsi, pencapaian dan detail lainnya.</p>
