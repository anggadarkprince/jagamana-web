                <div class="tab-pane fade" id="dashboard">
                    <h3 class="mtn">Dashboard</h3>
                    <p>Dasboard merupakan panel control dari halaman administrator berisi informasi statistik dan menu utama ketika user memasuki menu administrator.</p>
                    <h4>Companies</h4>
                    <p>
                        Terdapat fitur utama dalam dashboard yaitu, pengelolaan pegawai, administrator dapat daftar pegawai baru hingga lama, merubah status keaktifan user.
                        Pengelolaan company, fitur ini juga menampilkan daftar company dari yang terbaru hingga lama, setiap user dapat di aktifkan atau di suspend.
                        Sub menu dari fitur ini terdapat pengelolaan data master company field dan company size.
                    </p>
                    <h4>Jobs</h4>
                    <p>
                        Fitur job memberikan daftar job yang diciptakan oleh company. Administrator dapat melihat statistic jumlah job perhari, jumlah applicant yang apply ke job tersebut.
                        Terdapat pengelolaan data master yang berkaitan dengan job yaitu job field dan job level.
                    </p>
                    <h4>Applications</h4>
                    <p>
                        Fitur application adalah permintaan lamaran dari employee ke job yang di publish oleh company. Administrator menjadi perantara dengan menandai status mereka apakah approved atau rejected.
                    </p>
                    <h4>Thread</h4>
                    <p>
                        Fitur thread menampilkan daftar article yang dipublish di forum. Administrator dapat melihat daftar article yang telah aktif atau draft dari semua akun.
                    </p>
                    <h4>Feedback</h4>
                    <p>
                        Fitur feedback menghimpun semua pesan, keluhan, saran dan pertanyaan yang dikirip oleh user melalui menu contact di halaman public.
                    </p>
